package EjercicioFacturacion;

public class a4_Aparato {
	
	int voltaje;
	double precio;
	
	public a4_Aparato() {
		
	}

	public a4_Aparato(int voltaje, double precio) {
		super();
		this.voltaje = voltaje;
		this.precio = precio;
	}
 
	public int getVoltaje() {
		return voltaje;
	}

	public void setVoltaje(int voltaje) {
		this.voltaje = voltaje;
	}

	public double getPrecio() {
		return precio;
	}

	public void setPrecio(double precio) {
		this.precio = precio;
	}
	
	
	
	

}
